<h6>Documents Required:-</h6>

<h6>A) To purchase a house to be constructed / under construction / completed by a builder / developer</h6>

<ul>
	<li>Perfectly filled application in the prescribed format prepared by the bank</li>
	<li>Documents showing ownership of land / house</li>
	<li>Proof that the land is non-agricultural</li>
	<li>Letter issued by municipal Corporation and competent bodies for construction</li>
	<li>Certificate of completion of construction on completion of construction</li>
	<li>Attested copy of construction/blue print plan approved by competent authority</li>
	<li>Original letter issued in the name of the applicant regarding allotment of land / block</li>
	<li>Possession letter from builder.</li>
	<li>Original receipt of house purchase deed registered with the Registrar of Co-operatives</li>
	<li>Original receipt for registration of purchase deed</li>
	<li>Original receipts for the amount paid to the builder</li>
	<li>Original copy of No Objection Certificate issued by Builder / Developer for mortgage of Flat to Bank</li>
	<li>Certificate of Non-Encumbrance from the builder / developer</li>
	<li>Letter of consent from the partner to mortgage the flat to bank in case of joint purchase</li>
	<li>An indemnity bond for fulfilling the remaining documents as per the requirements of the bank within the stipulated time</li>
	<li>Power of Attorney to the Head of the Office for recovery of monthly salary from the member and co-applicant</li>
</ul>

<h6>B) To purchase houses from CIDCO / HUDCO / Housing Board / Improvement Trust / World Bank Scheme / MHADA / or other Boards / Institutions recognized by Central and State Governments</h6>

<ul>
	<li>Perfectly filled application in the prescribed format prepared by the bank</li>
	<li>Original letter issued by the competent authority regarding allotment of land / block in the name of the applicant</li>
	<li>Original purchase deed registered with the competent authority, indemnity bond for Registered mortgage deed to be made by the applicant to be given to the bank within the prescribed time</li>
	<li>Letter of consent from the partner to mortgage the flat to the bank in case of allotment of space in joint name from the competent authority</li>
	<li>Power of Attorney to the Head of the Office for recovery of monthly salary from the member and co-applicant</li>
</ul>

<h6>C) To purchase flats allotted under the government&#39;s discretionary scheme</h6>

<ul>
	<li>Perfectly filled application in the prescribed format prepared by the bank</li>
	<li>Original letter issued by the government regarding allotment of land / block in the name of the applicant under the voluntary scheme</li>
	<li>indemnity bond of the applicant for payment of the original purchase deed registered with the builder, registered mortgage to be made by the applicant to the bank within the prescribed time</li>
	<li>Power of Attorney to the Head of the Office for recovery of monthly salary from the member and co-applicant</li>
	<li>Letter of consent from the partner to mortgage the flat to the bank in case of allotment of space in joint name from the competent authority</li>
</ul>

<h6>D) To repay the loan of flats purchased with loan from Government of Maharashtra and other financial institutions</h6>

<ul>
	<li>Original letter issued regarding details of outstanding loans from Government of Maharashtra and other financial institutions</li>
	<li>Certified copy of original documents submitted by the member to the organization from which the loan has been taken</li>
	<li>indemnity bond from the applicant regarding the mortgage to be made by the applicant, original documents of the house submitted to the concerned institution for borrowing</li>
	<li>Letter of consent from the partner to mortgage the flat to the bank in case of allotment of space in joint name from the competent authority</li>
</ul>

<h6>E) To purchase a house / flat available for resale</h6>

<ul>
	<li>Perfectly filled application in the prescribed format prepared by the bank</li>
	<li>Documents showing original rights of plot / house</li>
	<li>Original receipt of registered purchase deed made with the original owner</li>
	<li>Receipts for the amount paid to the original owner</li>
	<li>Original receipts for purchases made by anyone who has made a purchase transaction with the builder before the original owner ( Chain of agreement )</li>
	<li>No Objection Certificate issued by the concerned organization for mortgage a flat to bank if the flat to be purchased is in a registered co-operative society.</li>
	<li>Non Encumbrance certificate, If the place to be purchased is in a registered co-operative society</li>
	<li>Letter of consent from the partner to mortgage the flat to the bank in case of allotment of space in joint name from the competent authority</li>
</ul>

<h6>F) Self-owned / owned by co-operative housing society or to construct a new house for own residence on the land allotted by them</h6>

<ul>
	<li>Perfectly filled application in the prescribed format prepared by the bank</li>
	<li>If you want to purchase land and build on this land, copy of purchase deed of registered land</li>
	<li>Documents showing ownership of plot / house</li>
	<li>7/12 extract of original land</li>
	<li>Non Agriculture Certificate of competent authority</li>
	<li>Approval of construction by competent authorities</li>
	<li>Blue print of construction by competent authorities</li>
	<li>Estimated cost of construction by architect</li>
	<li>Certified copy of the contract with the contractor appointed for construction</li>
	<li>Allotment letter from the co-op society if the plot is in that society</li>
	<li>copy of lease agreement</li>
	<li>Permission letter of mortgage of plot to bank</li>
	<li>Letter of consent from the partner to mortgage the flat to the bank in case of allotment of space in joint name from the competent authority</li>
</ul>

<h6>G) Documents to be submitted for incremental construction</h6>

<ul>
	<li>Certificate of cost from architect / engineer / contractor for additional construction</li>
	<li>license obtained from the competent authority for reference work</li>
	<li>A copy of the plan certified by the architect / engineer for additional construction</li>
</ul>

<h6>H) Other than document&rsquo;s mentioned in (A) to (G), following documents to be attached with loan application</h6>

<ul>
	<li>Salary slip of applicant / co-applicant of last three years</li>
	<li>Latest salary slip of sureties</li>
	<li>Affidavit in prescribed form stating that the applicant / co-applicant has not taken any loan from other banks as well as other financial institutions till date.</li>
	<li>Address proof of applicant / co-applicant</li>
	<li>Guarantee in the prescribed form from sureties regarding recovery of arrears from the sureties if the applicant is in arrears for any reasons</li>
	<li>Check Bank account, passbook copy and other bank deposit details (If any)</li>
	<li>Details of loans in the name of the applicant at present in addition to the home loan remaining in the bank &amp; details of loans from other banks and financial institutions</li>
	<li>Details of applicants insurance policy</li>
</ul>

